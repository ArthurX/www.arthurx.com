 <P>

After one week in Barcelona we went to the beach.<br>

It's a long beach in Taragona.<br>

First we camped three meters from the railway.<br>

That's an ear-deafening sound.<br>

Sam while asleep shook.<br>

So the next night we went to a place at the beach front.<br>

No privacy but the calming sound of the waves.

<p>

If one walked further north from the beach where all campsites were,<br>

one reached a rock that grew into a cliff.<br>

And by climbing that cliff and passing the cape and enjoying the wide views<br>

over water, sea and land <br>

passing under the pine trees moved by the midday wind <br>

and over bare rock licked by the highest waves,<br>

we came to that other beach.<br>

Secluded, finding a path winding down the overhanging walls.<br>

Remote, hot enclosed, burning sun, silent, no signs of civilication.<br>

<p>It proved to be the local nudist beach.<br>

Forbidden, every moment you could expect Louis de Funais and <br>Les Gendarmes de St Tropéz

to jump out of the trees and arrest you.

<p>If this painting should portait anything,<br>

It should be the enclosedness, the heat and the girls far away.

